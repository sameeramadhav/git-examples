package com.javatechie.mathoperation.git_examples;

public class Add {

	private int value1;
	private int value2;
	private int value3;

	public Add(int value1, int value2, int value3) {
		this.value1 = value1;
		this.value2 = value2;
		this.value3 = value3;

	}

	public int sum() {
		return value1 + value2 + value3;
	}
}
